package com.linhan.bai.day09;

import java.util.Date;

/**
 * Object类中toString()的使用：
 * 1.当我们输出一个对象的引用时，实际上就是调用当前对象的toString()方法
 * 2.Object类中toString()的定义
 * 		public String toString() {
        return getClass().getName() + "@" + Integer.toHexString(hashCode());
    }
 * 3.像String、Date、File等包装类中都重写了Object类中的toString()方法，使得在调用对象的toString()方法时，返回"实体内容"信息
 * 4.自定义类也可以重写toString()方法，当调用此方法时，返回对象的"实体内容"
 * 
 * @author ZhangShengjie
 *
 * @date 2020年6月29日上午12:07:51
 */
public class ToStringTest {
	public static void main(String[] args) {
		Customer customer = new Customer("Tom",21);
		System.out.println(customer.toString());
		System.out.println(customer);
		
		String str = new String("MM");
		System.out.println(str);//MM
		
		Date date = new Date(1241234251523L);
		System.out.println(date.toString());
		
	}
}
